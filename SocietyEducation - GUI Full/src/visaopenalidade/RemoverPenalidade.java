    public final void preencherCombo(){
        ArrayList<Penalidade> res = con.consultarPenalidades();
        penalidade.removeAllItems();
        int i = 0;
        for(Penalidade c : res) {
            penalidade.insertItemAt(c.getCod_penalidade()+" - "+c.getTipo(), i);
            i++;
        }
    }
