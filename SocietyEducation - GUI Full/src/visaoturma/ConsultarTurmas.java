    private void exibirCursosNaTabela() {
            DefaultTableModel model = (DefaultTableModel)tabela.getModel();
            model.setRowCount(0); // Limpar a tabela antes de preenchÃª-la novamente
            for (Turma turma : controle.consultarTurmas()) {
                model.addRow(new Object[]{turma.getCod_turma(), turma.getNome_turma(), turma.getAno(), turma.getPeriodo(), turma.getCod_curso()});
            }
        }

