    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(curso.getSelectedIndex() < 0)
            JOptionPane.showMessageDialog(null, "Selecionar um curso");
        else{
            String[] cod = curso.getSelectedItem().toString().split(" - ");
            CursoControle ins = new CursoControle();
            String res = ins.removerCurso(Integer.parseInt(cod[0]));
            JOptionPane.showMessageDialog(null, res);
            preencherCombo();
        }
            
            
            
            
    }//GEN-LAST:event_jButton1ActionPerformed

