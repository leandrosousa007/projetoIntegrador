    public final void preencherCombo(){
        ArrayList<Curso> res = con.consultarCursos();
        curso.removeAllItems();
        int i = 0;
        for(Curso c : res){
            curso.insertItemAt(c.getCod_curso()+ " - " + c.getNome_curso(), i );
            i++;  
        }
    }
