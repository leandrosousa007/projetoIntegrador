    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Curso cursos = new Curso();
        cursos.setNome_curso(nomei.getText());
        cursos.setSigla(siglai.getText());
        
        String[] res = curso.getSelectedItem().toString().split(" - ");
        cursos.setCod_curso(Integer.parseInt(res[0]));
        CursoControle ins = new CursoControle();
        String resu = ins.alterarCurso(cursos);
        JOptionPane.showMessageDialog(null, resu);
        nomei.setText("");
        siglai.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

