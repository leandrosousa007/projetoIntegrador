     public final void preencherCombo(){
        ArrayList<AlunoTurma> res = con.consultarAlunosTurmas();
        al.removeAllItems();
        int i = 0;
        for(AlunoTurma c : res) {
            al.insertItemAt(c.getCod_aluno_turma()+" - "+c.getData_matricula(), i);
            i++;
        }
    }
