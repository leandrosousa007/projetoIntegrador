    public final void preencherCombo(){
        ArrayList<Ocorrencia> res = con.consultarOcorrencias();
        al.removeAllItems();
        int i = 0;
        for(Ocorrencia c : res) {
            al.insertItemAt(c.getCod_ocorrencia()+" - "+c.getCampus(), i);
            i++;
        }
    }
