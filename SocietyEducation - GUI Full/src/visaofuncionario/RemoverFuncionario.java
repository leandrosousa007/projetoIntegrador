    public final void preencherCombo(){
        ArrayList<Funcionario> res = con.consultarFuncionarios();
        nome.removeAllItems();
        int i = 0;
        for(Funcionario c : res){
            nome.insertItemAt(c.getCod_funcionario()+ " - " + c.getNome(), i );
            i++;  
        }
    }

