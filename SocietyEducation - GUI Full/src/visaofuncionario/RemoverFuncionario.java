    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(nome.getSelectedIndex() < 0)
        JOptionPane.showMessageDialog(null, "Selecionar um funcionario");
        else{
            String[] cod = nome.getSelectedItem().toString().split(" - ");
            FuncionarioControle ins = new FuncionarioControle();
            String res = ins.removerFuncionario(Integer.parseInt(cod[0]));
            JOptionPane.showMessageDialog(null, res);
            preencherCombo();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

