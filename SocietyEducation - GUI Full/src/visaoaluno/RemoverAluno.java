    public final void preencherCombo(){
        ArrayList<Aluno> res = con.consultarAlunos();
        al.removeAllItems();
        int i = 0;
        for(Aluno c : res) {
            al.insertItemAt(c.getCod_aluno()+" - "+c.getNome(), i);
            i++;
        }
    }
